def get_total_stock_count():
    """전체 종목 수를 반환합니다."""
    # 단순 합계 (중복 포함)
    return sum(len(stocks) for stocks in sector_stocks.values())

def get_all_stock_names():
    """모든 종목 이름 리스트를 반환합니다. 중복 제거."""
    stock_names = []
    for sector, stocks in sector_stocks.items():
        for stock_name, _ in stocks:
            stock_names.append(stock_name)
    return list(set(stock_names)) # 중복 제거

def get_sector_names():
    """섹터 이름 리스트를 반환합니다."""
    return list(sector_stocks.keys())

